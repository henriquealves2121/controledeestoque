    public void ListaDeFornecedor(){
        jComboBoxFornecedor.removeAllItems();
        res = MySQL.ListaFornecedores();
        if( res!=null ) {
            try {
                while(res.next()) {
                    jComboBoxFornecedor.addItem(res.getString("id_fornecedor") + "  -  " +res.getString("razao_social"));
		}
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Problema para exibir registros!");
            }
	} else {
            JOptionPane.showMessageDialog(null, "NÃ£o ha fornecedores cadastrados!");
	}
    }
