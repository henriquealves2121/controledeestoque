    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        // TODO add your handling code here:
        
        aux = (String) jComboBoxProduto.getSelectedItem();
        aux = aux.substring(0,1);
        id_produto = Integer.valueOf(aux);
        
        quantidade = (int) jSpinnerQuantidade.getValue();
        valor_unidade = Float.valueOf(jTextFieldValorUnidade.getText());
        
        BD = MySQL.CadastrarComprar(id_produto, quantidade, valor_unidade);
       Adiciona_Estoque(id_produto, quantidade);
       
        if ( BD == 1 ){
            Home.ok();
            Limpar();
        }else{
            Home.erro();
        }
    }//GEN-LAST:event_jButtonEnviarActionPerformed

